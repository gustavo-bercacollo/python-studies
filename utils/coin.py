
def half_price(value):
    half = value / 2
    return half

def double_price(value):
    double  = value * 2
    return double

def increase_ten_percent(value):
    percent = (value * (10/100)) + value
    return  percent

def decreases_thirteen_percent(value):
    percent =  value - (value * (13 / 100))
    return percent
